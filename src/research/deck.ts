// Tiny deck controller: prev/next/dot buttons (all plain <button data-dwell>,
// so the gesture wall's dwell selector drives them) + arrow-key navigation.
const DECK_JS = `
(function () {
  var slides = Array.prototype.slice.call(document.querySelectorAll("[data-slide]"));
  var dots = Array.prototype.slice.call(document.querySelectorAll("[data-dot]"));
  var current = 0;
  function show(index) {
    current = (index + slides.length) % slides.length;
    slides.forEach(function (slide, i) { slide.classList.toggle("active", i === current); });
    dots.forEach(function (dot, i) { dot.classList.toggle("active", i === current); });
  }
  document.querySelectorAll("[data-nav]").forEach(function (button) {
    button.addEventListener("click", function () {
      show(current + (button.getAttribute("data-nav") === "next" ? 1 : -1));
    });
  });
  dots.forEach(function (dot, i) { dot.addEventListener("click", function () { show(i); }); });
  document.addEventListener("keydown", function (event) {
    if (event.key === "ArrowRight" || event.key === " ") show(current + 1);
    if (event.key === "ArrowLeft") show(current - 1);
  });
})();
`;
