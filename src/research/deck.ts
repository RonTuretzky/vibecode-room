// Research dossier deck: one completed quest + its report in, ONE complete
// self-contained projector-ready HTML slideshow out — inline CSS, a small
// inline deck controller, no external assets. Modeled on the pitch-deck
// template (src/slideshow/template.ts): every model/user-influenced string is
// escaped HERE; the only raw injection is the QR SVG, which this server
// generates itself from the (validated http/https) source URL.
//
// Slides: 1. the claim as heard · 2. findings with verdicts · 3. bias &
// blind spots · 4. sources with scan-me QR codes · 5. keep digging.

import { escapeHtml } from "../slideshow/template";
import type { ResearchQuest, ResearchReport, ResearchVerdict } from "./types";

// One source prepared for rendering: the report source + its QR SVG (already
// generated by the caller via src/publish/qr.ts — the deck stays sync + pure).
export interface DeckSource {
  title: string;
  url: string;
  publisher: string;
  note: string;
  qrSvg: string;
}

export interface ResearchDeckInput {
  quest: ResearchQuest;
  report: ResearchReport;
  sources: readonly DeckSource[];
}

const VERDICT_LABEL: Record<ResearchVerdict, string> = {
  supported: "SUPPORTED",
  refuted: "REFUTED",
  mixed: "MIXED",
  unverified: "UNVERIFIED",
};

export function renderResearchDeckHtml(input: ResearchDeckInput): string {
  const { quest, report } = input;
  const slides = [
    heroSlide(quest, report),
    findingsSlide(report),
    biasSlide(report),
    sourcesSlide(input.sources),
    followUpSlide(report),
  ];
  const title = `${quest.topic} — research`;
  const footer = `${quest.id} · ${quest.kind} · vibersyn research`;
  return [
    "<!doctype html>",
    '<html lang="en">',
    "<head>",
    '<meta charset="utf-8" />',
    '<meta name="viewport" content="width=device-width, initial-scale=1" />',
    `<title>${escapeHtml(title)}</title>`,
    `<style>${DECK_CSS}</style>`,
    "</head>",
    "<body>",
    '<main class="deck" data-deck>',
    slides
      .map(
        (slide, index) =>
          `<section class="slide${index === 0 ? " active" : ""}" data-slide="${index}">${slide}</section>`,
      )
      .join("\n"),
    "</main>",
    '<nav class="deck-nav">',
    '<button type="button" class="nav-btn" data-nav="prev" data-dwell="deck-prev" aria-label="Previous slide">‹</button>',
    `<div class="dots">${slides.map((_, index) => `<button type="button" class="dot${index === 0 ? " active" : ""}" data-dot="${index}" data-dwell="deck-dot-${index}" aria-label="Slide ${index + 1}"></button>`).join("")}</div>`,
    '<button type="button" class="nav-btn" data-nav="next" data-dwell="deck-next" aria-label="Next slide">›</button>',
    "</nav>",
    `<footer class="deck-footer">${escapeHtml(footer)}</footer>`,
    `<script>${DECK_JS}</script>`,
    "</body>",
    "</html>",
  ].join("\n");
}

function heroSlide(quest: ResearchQuest, report: ResearchReport): string {
  const heard =
    quest.contextSpan.quote.trim().length > 0
      ? `<p class="heard">Heard in the room: <q>${escapeHtml(quest.contextSpan.quote)}</q></p>`
      : "";
  return [
    `<span class="kicker">Researched from the room · ${escapeHtml(quest.kind)}</span>`,
    `<h1>${escapeHtml(quest.topic)}</h1>`,
    `<blockquote class="claim">${escapeHtml(quest.claim)}</blockquote>`,
    heard,
    `<p class="confidence conf-${escapeHtml(report.confidence)}">research confidence: ${escapeHtml(report.confidence)}</p>`,
  ].join("\n");
}

function findingsSlide(report: ResearchReport): string {
  const findings =
    report.findings.length === 0
      ? '<p class="empty">No discrete findings — see the summary above.</p>'
      : `<ul class="findings">${report.findings
          .map(
            (finding) =>
              `<li class="finding"><span class="verdict verdict-${escapeHtml(finding.verdict)}">${VERDICT_LABEL[finding.verdict]}</span>` +
              `<div class="finding-body"><p class="finding-claim">${escapeHtml(finding.claim)}</p>` +
              (finding.explanation.length > 0 ? `<p class="finding-why">${escapeHtml(finding.explanation)}</p>` : "") +
              (finding.sourceIndexes.length > 0
                ? `<p class="finding-src">sources ${finding.sourceIndexes.map((index) => `#${index + 1}`).join(", ")}</p>`
                : "") +
              "</div></li>",
          )
          .join("")}</ul>`;
  return [
    '<span class="kicker">What we found</span>',
    "<h1>Findings, fact-checked</h1>",
    `<p class="summary">${escapeHtml(report.summary)}</p>`,
    findings,
  ].join("\n");
}

function biasSlide(report: ResearchReport): string {
  const notes =
    report.biasNotes.length === 0
      ? '<p class="empty">No significant bias signals were flagged — stay skeptical anyway.</p>'
      : `<ul class="bias-notes">${report.biasNotes
          .map(
            (note) =>
              `<li class="bias-note"><span class="severity severity-${escapeHtml(note.severity)}">${escapeHtml(note.severity)}</span>` +
              `<p>${escapeHtml(note.note)}</p></li>`,
          )
          .join("")}</ul>`;
  return ['<span class="kicker">Read with care</span>', "<h1>Bias &amp; blind spots</h1>", notes].join("\n");
}

function sourcesSlide(sources: readonly DeckSource[]): string {
  const cards =
    sources.length === 0
      ? '<p class="empty">No live sources were captured for this quest.</p>'
      : `<div class="sources">${sources
          .map(
            (source, index) =>
              '<article class="source-card">' +
              `<div class="source-qr" aria-label="QR code for ${escapeHtml(source.url)}">${source.qrSvg}</div>` +
              `<div class="source-meta"><span class="source-index">#${index + 1}</span>` +
              `<h2>${escapeHtml(source.title)}</h2>` +
              (source.publisher.length > 0 ? `<p class="source-pub">${escapeHtml(source.publisher)}</p>` : "") +
              (source.note.length > 0 ? `<p class="source-note">${escapeHtml(source.note)}</p>` : "") +
              `<p class="source-url">${escapeHtml(source.url)}</p></div></article>`,
          )
          .join("")}</div>`;
  return ['<span class="kicker">Scan to read</span>', "<h1>Sources</h1>", cards].join("\n");
}

function followUpSlide(report: ResearchReport): string {
  const items =
    report.followUps.length === 0
      ? '<p class="empty">Nothing queued — the room decides where to dig next.</p>'
      : `<ul class="followups">${report.followUps.map((item) => `<li>${escapeHtml(item)}</li>`).join("")}</ul>`;
  return ['<span class="kicker">Keep digging</span>', "<h1>Open questions</h1>", items].join("\n");
}

